package code16.approach1_overloading;

public class IntSum {
  public static int sumUp(int a, int b) {
    return a + b;
  }

  public static int sumUp(int a, int b, int c) {
    return a + b + c;
  }

  public static int sumUp(int a, int b, int c, int d) {
    return a + b + c + d;
  }

  public static int sumUp(int a, int b, int c, int d, int e) {
    return a + b + c + d + e;
  }
}
